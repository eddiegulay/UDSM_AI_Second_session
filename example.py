# example uses of classes (Built-in classes)


# String class is a built-in class
the_string = "I am A MixEd CaSe StRiNg"

# String class has a method called upper() that converts all the characters in a string to uppercase
print("Upper Case String: ", the_string.upper())
# String class has a method called lower() that converts all the characters in a string to lowercase
print("Lower Case String: ",the_string.lower())
# String class has a method called capitalize() that converts the first character in a string to uppercase
print("Original String: ", the_string)


# lists ( Strangers call them arrays )
the_list = [2, 4, 3, 6, 7, 1, 8, 5, 9, 10]

# list class has a method called append() that adds an item to the end of a list
print("Appended List: ", the_list)

# list class has a method called index that returns the index of an item in a list

# example searching for an item in a list
print("Index of 5: ", the_list.index(5))

